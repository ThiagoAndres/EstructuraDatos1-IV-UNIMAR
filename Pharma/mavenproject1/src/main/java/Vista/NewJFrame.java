    private void jBGuardarMedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarMedActionPerformed
        if(jTCodigoMed.getText().equals("") || jTNombreMed.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe completar los campos para agregar un medicamento!","Advertencia",JOptionPane.WARNING_MESSAGE);
            
        } else{
            int codigo = Integer.parseInt(jTCodigoMed.getText());
            String nombre = jTNombreMed.getText();
            String tipo = jTTipoMed.getText();;
            float precio= Float.parseFloat(jTPrecioMed.getText());
                  
            Medicamento medicamento = new Medicamento(codigo,nombre, tipo, precio );
            jCNombreMed.addItem(nombre);
            farmacia.addMedicamento(medicamento);
            
    
        }    
    }//GEN-LAST:event_jBGuardarMedActionPerformed

